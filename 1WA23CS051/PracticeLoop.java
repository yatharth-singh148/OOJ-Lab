import java.io.*;

public class PracticeLoop
{
	public static void main(String args[])
	{
		System.out.println("For loop:");
		int i;
		for(i=0;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("While loop:");
		i=0;
		while(i<11)
		{
			i++;
			System.out.println(i);
		}
	}
}
